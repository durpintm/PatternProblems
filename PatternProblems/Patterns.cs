namespace PatternProblems
{
    internal class Patterns
    {
        public void Pattern1()
        {
            for (int i = 0; i < 5; i++)
            {
                for (int j = 0; j < 5; j++)
                {
                    Console.Write("*");
                }
                Console.WriteLine("\n");

            }
        }
    }
}
